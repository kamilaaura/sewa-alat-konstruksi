function addToCart(productId) {
    let product = products.find(p => p.id === productId);
    let existingItem = cart.find(p => p.id === productId);

    if (existingItem) {
        existingItem.jumlah += 1;
    } else {
        cart.push({ id: product.id, nama: product.nama, harga: product.harga, jumlah: 1, durasi: 1 });
    }
    updateCart();
}

function updateCart() {
    let cartContainer = document.getElementById("cart-items");
    console.log(document.getElementById("total-price")); // Harus tidak null
    let totalHarga = 0;
    cartContainer.innerHTML = "";

    cart.forEach((item, index) => {
        let subtotal = item.harga * item.jumlah * item.durasi;
        totalHarga += subtotal;

        cartContainer.innerHTML += `
            <div class="cart-item">
                <span>${item.nama} - Rp ${item.harga.toLocaleString()} x </span>

                <label for="jumlah-${index}">Jumlah:</label>
                <input type="number" min="1" value="${item.jumlah}" id="jumlah-${index}" onchange="updateQuantity(${index})">

                <span> x </span>

                <label for="durasi-${index}">Durasi (hari):</label>
                <input type="number" min="1" value="${item.durasi}" id="durasi-${index}" onchange="updateDuration(${index})">

                <span> = Rp ${subtotal.toLocaleString()}</span>
                <button onclick="removeFromCart(${index})">Hapus</button>
            </div>`;
    });

    document.getElementById("total-price").textContent = `Total Sewa: Rp ${totalHarga.toLocaleString()}`;
    document.getElementById("checkout-btn").disabled = cart.length === 0;
}

function updateQuantity(index) {
    let jumlahInput = document.getElementById(`jumlah-${index}`);
    cart[index].jumlah = parseInt(jumlahInput.value);
    updateCart();
}

function updateDuration(index) {
    let durasiInput = document.getElementById(`durasi-${index}`);
    cart[index].durasi = parseInt(durasiInput.value);
    updateCart();
}

function removeFromCart(index) {
    cart.splice(index, 1);
    updateCart();
}

